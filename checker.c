#include "so_long.h"

int	is_ber(char *file)
{
	int	i;

	i = 0;
	while (file[i] != '.' && file[i])
		i++;
	if (file[i] == '.' && file[i + 1] == 'b' && file[i + 2] == 'e'
		&& file[i + 3] == 'r' && !file[i + 4])
		return (1);
	return (0);
}
